"""
Este é um programa que converte M/s em KM/h
"""
# Fórmula : K = M * 3.6
print('Digite a Velocidade em M/s:')
metrosSegundo = (int(input()))
kilometrosHora = metrosSegundo * 3.6
print(f'A velocidade em Km/h é de: {kilometrosHora:.2f} Km/h')
# Assim que se usa o '.2f'                          ^^;

