"""
17. Este é um programa que converte centimetros para polegadas.
"""

# Fórmula : P = C / 2.54

print('Seja Bem vindo ao programa que converte centímetros em  polegadas .')

print("Por favor digite o valor em centimetros :")

centimetros = float(input())
polegadas = centimetros / 2.54

print(f'{centimetros:.1f} centimetros é  {polegadas:.1f} polegadas')

