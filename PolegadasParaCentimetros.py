"""
16. Este é um programa que converte polegadas para centímetros.
"""

# Fórmula : C = P * 2.54

print('Seja Bem vindo ao programa que converte polegadas em centímetros .')

print("Por favor digite o valor em polegadas :")

polegadas = float(input())
centimetros = polegadas * 2.54

print(f'{polegadas:.1f} polegadas é  {centimetros:.1f} centimetros')
