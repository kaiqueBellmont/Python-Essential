"""
Este é um programa em que se tranforma uma string em lista,
depois soma-se os algarismos em meio a um cast.
"""


print("Digite um numero:")
numero = str(input())

numero.split()

soma = int(numero[0]) + int(numero[1]) + int(numero[2])

if int(numero) == 666:
    print("Não faça isso, Jesus te ama ♥.")
    print(f'A soma é {soma}')
else:
    print(f'A soma dos algarismos do numero {numero} é {soma}')
    print(f'Porque {int(numero[0])} + {int(numero[1])} + {int(numero[2])} = {soma}')




















