print('Este é um programa que converte a temperatura  Fahrenheit em Celsius  ')
# Formula : C = 5.0 * (F - 32.0) / 9.0
print('Por favor Digite a temperatura em fahrenheit :')
fahrenheit = (float(input()))
celsius = 5.0 * (fahrenheit - 32.0) / 9.0
print(f'A temperatura em celsius é de {celsius}° ')

