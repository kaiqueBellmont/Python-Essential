print('Este é um programa que converte a temperatura Celsius em Fahrenheit ')
# Formula : F = C*(9.0/5.0)+32.0
print('Por favor Digite a temperatura em Celsius :')
celsius = (float(input()))
fahrenheit = celsius * (9.0/5.0) + 32.0
print(f'A temperatura em fahrenheit é de {fahrenheit}° ')

