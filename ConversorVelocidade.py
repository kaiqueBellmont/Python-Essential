"""
Este é um programa que converte KM/h em M/s
"""
# Fórmula : M = K/3.6
print('Digite a Velocidade em Km/h:')
kilometrosHora = (int(input()))
metrosSegundo = kilometrosHora / 3.6
print(f'A velocidade em M/s é de: {metrosSegundo:.2f}M/s')
# Assim que se usa o '.2f'                        ^^;



